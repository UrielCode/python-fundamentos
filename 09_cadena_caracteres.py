nombre = input('Cual es tu nombre: ')

# Método (es una función para un tipo de dato en particular)

# Texto en mayúsculas
nombre= nombre.upper()

# Texto en minúsculas
nombre = nombre.lower()

# Texto con la primera letra en mayúsculas
nombre = nombre.capitalize()

# Eliminar espacios basura
nombre = nombre.strip()

# Remplazar letras
nombre = nombre.replace('o', 'a')

# Indice de una palabra
nombre = nombre[0]

# Cuantos carectes tiene una cadena
nombre = len(nombre)

input(nombre)