# Operadores lógicos

es_estudiante = True
trabaja = False

# Operador and (Y)
es_estudiante and trabaja # False

# Operado or (Ó)
es_estudiante or trabaja # True

# Operador Not (invierte el valor de una variable)
not trabaja # True

# Operadores de comparación
num1 = 5
num2 = 5

# Signo de igualdad (==)
num1 == num2 # True

# Distinto de 
num1 = 5
num2 = 3

num1 != num2 # True

# Mayor que
num1 > num2 #True

# Menor que
num1 < num2 # False

# Mayor o igual que
num1 >= num2 #True

# Menor o igual que 
num1 <= num2 # False